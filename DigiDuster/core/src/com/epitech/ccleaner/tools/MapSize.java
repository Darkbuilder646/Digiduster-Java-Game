package com.epitech.ccleaner.tools;

public class MapSize {

  public final float width;

  public final float height;

  public final float x;

  public final float y;

  public MapSize(float width, float height, float x, float y) {
    this.width = width;
    this.height = height;
    this.x = x;
    this.y = y;
  }

}
